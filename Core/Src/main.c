#define buflen 768

Synth syn;
float recl[buflen / 2];
float recr[buflen / 2];
float bufl[buflen / 2];
float bufr[buflen / 2];

float hpvl = 0; // ç»™audio inåŠ ä¸ªé«˜é?šç”¨çš?
float hpvr = 0;
float hpctof = 50 / 48000;

void __attribute__((section("ITCMRAM"))) processBlock()
{
  while (HasMidiMsg())
  {
    SynthProcMidi(&syn, GetMidiMsg());
  }

  int numSamples = codecGetNumSamples();
  int32_t *recbuf1 = codecGetRx1Ptr();
  int32_t *wavbuf1 = codecGetTx1Ptr();
  int32_t *recbuf2 = codecGetRx2Ptr();
  int32_t *wavbuf2 = codecGetTx2Ptr();
  int32_t *recbuf3 = codecGetRx3Ptr();
  int32_t *wavbuf3 = codecGetTx3Ptr();
  float to_int = (int)1 << 22;
  float div_to_int = 1.0 / to_int;
  for (int i = 0, j = 0; i < numSamples; i += 2, j++)
  {
    float vl = recbuf1[i + 0] * div_to_int;
    float vr = recbuf1[i + 1] * div_to_int;
    hpvl += hpctof * (vl - hpvl);
    hpvr += hpctof * (vr - hpvr);
    recl[j] = vl - hpvl;
    recr[j] = vr - hpvr;
  }
  SynthProcessBlock(&syn, recl, recr, bufl, bufr, buflen / 2);
  for (int i = 0, j = 0; i < numSamples; i += 2, j++)
  {
    wavbuf1[i + 0] = bufl[j] * to_int * 0.25;
    wavbuf1[i + 1] = bufr[j] * to_int * 0.25;
  }
}

